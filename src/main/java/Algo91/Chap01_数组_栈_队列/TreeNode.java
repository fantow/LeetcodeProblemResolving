package Algo91.Chap01_数组_栈_队列;

public class TreeNode {
    int val;

    TreeNode left;

    TreeNode right;

    TreeNode(int x){
        val = x;
    }

    @Override
    public String toString() {
        return "TreeNode{" +
                "val=" + val +
                ", left=" + left +
                ", right=" + right +
                '}';
    }
}
